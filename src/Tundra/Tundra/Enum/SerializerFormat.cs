namespace Tundra.Enum
{
    /// <summary>
    /// Serializer Format Enum
    /// </summary>
    public enum SerializerFormat
    {
        /// <summary>
        /// The json serializer will be used
        /// </summary>
        JSON,

        /// <summary>
        /// The XML serializer will be used
        /// </summary>
        XML,

        /// <summary>
        /// Specifies that no serialization should occur
        /// </summary>
        None
    }
}